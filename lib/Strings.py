# Creating a string
my_string = "Hello, World!"

# Accessing characters
print(my_string[0])  # Prints 'H'

# Slicing strings
substring = my_string[7:12]  # Gets 'World'

# String concatenation
new_string = my_string + " How are you?"
print(new_string)  # Prints "Hello, World! How are you?"

# String formatting
name = "Alice"
age = 30
formatted_str = f"My name is {name} and I am {age} years old."
print(formatted_str)
# Prints "My name is Alice and I am 30 years old."
